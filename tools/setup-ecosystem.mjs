import { spawnSync } from 'node:child_process'
import { promises as fs } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const ROOT = path.resolve(__dirname, '..')

const args = new Set(process.argv.slice(2))
const skipInstall = args.has('--skip-install')
const skipEnv = args.has('--skip-env')

const npmCmd = process.platform === 'win32' ? 'npm.cmd' : 'npm'

const PROJECTS = [
  { name: 'Nexus Main', dir: 'Nexus Main' },
  { name: 'Nexus Mobile', dir: 'Nexus Mobile' },
  { name: 'Nexus Code', dir: 'Nexus Code' },
  { name: 'Nexus Code Mobile', dir: 'Nexus Code Mobile' },
  { name: 'Nexus Control', dir: 'Nexus Control' },
  { name: 'API/nexus-control-plane', dir: 'API/nexus-control-plane' },
]

const APP_ENV_TARGETS = [
  { appId: 'main', dir: 'Nexus Main' },
  { appId: 'mobile', dir: 'Nexus Mobile' },
  { appId: 'code', dir: 'Nexus Code' },
  { appId: 'code-mobile', dir: 'Nexus Code Mobile' },
]

const DEFAULT_CONTROL_URL = process.env.NEXUS_CONTROL_URL || 'http://localhost:4399'

const run = (cmd, cmdArgs, cwd) => {
  const printable = [cmd, ...cmdArgs].join(' ')
  console.log(`\n$ ${printable}`)

  const result = spawnSync(cmd, cmdArgs, {
    cwd,
    stdio: 'inherit',
    env: process.env,
  })

  if (result.error) {
    throw result.error
  }

  if (result.status !== 0) {
    throw new Error(`Command fehlgeschlagen (${result.status}): ${printable}`)
  }
}

const fileExists = async (target) => {
  try {
    await fs.access(target)
    return true
  } catch {
    return false
  }
}

const ensureEnvFile = async ({ appId, dir }) => {
  const file = path.join(ROOT, dir, '.env.local')
  const defaults = {
    VITE_NEXUS_CONTROL_URL: DEFAULT_CONTROL_URL,
    VITE_NEXUS_CONTROL_INGEST_KEY: `${appId}-local-dev-key`,
  }

  if (!(await fileExists(file))) {
    const body = [
      '# Auto-generated by tools/setup-ecosystem.mjs',
      ...Object.entries(defaults).map(([key, value]) => `${key}=${value}`),
      '',
    ].join('\n')
    await fs.writeFile(file, body, 'utf8')
    return { file, mode: 'created', addedKeys: Object.keys(defaults) }
  }

  const raw = await fs.readFile(file, 'utf8')
  const hasKey = (key) => new RegExp(`^\\s*${key}\\s*=`, 'm').test(raw)
  const missing = Object.entries(defaults).filter(([key]) => !hasKey(key))

  if (missing.length === 0) {
    return { file, mode: 'unchanged', addedKeys: [] }
  }

  const appendix = [
    '',
    '# Added by tools/setup-ecosystem.mjs',
    ...missing.map(([key, value]) => `${key}=${value}`),
    '',
  ].join('\n')

  await fs.appendFile(file, appendix, 'utf8')
  return {
    file,
    mode: 'updated',
    addedKeys: missing.map(([key]) => key),
  }
}

const main = async () => {
  console.log('Nexus Ecosystem Setup')
  console.log('=====================')

  if (!skipInstall) {
    for (const project of PROJECTS) {
      const projectDir = path.join(ROOT, project.dir)
      console.log(`\nInstalliere Dependencies: ${project.name}`)
      run(npmCmd, ['install'], projectDir)
    }
  } else {
    console.log('\nInstall-Schritt uebersprungen (--skip-install).')
  }

  const envResults = []
  if (!skipEnv) {
    for (const target of APP_ENV_TARGETS) {
      const result = await ensureEnvFile(target)
      envResults.push(result)
    }
  } else {
    console.log('\nEnv-Schritt uebersprungen (--skip-env).')
  }

  console.log('\nSetup abgeschlossen.')

  if (envResults.length > 0) {
    console.log('\n.env.local Status:')
    for (const result of envResults) {
      const rel = path.relative(ROOT, result.file)
      const keys = result.addedKeys.length > 0 ? ` (${result.addedKeys.join(', ')})` : ''
      console.log(`- ${rel}: ${result.mode}${keys}`)
    }
  }

  console.log('\nNaechste Schritte:')
  console.log('- npm run dev:control:open  # startet Control Plane + UI mit Browser-Open')
  console.log('- npm run dev:main          # startet Nexus Main mit Electron')
  console.log('- npm run verify:ecosystem  # fuehrt Integritaetspruefungen aus')
}

main().catch((error) => {
  console.error('\nSetup fehlgeschlagen:')
  console.error(error.message || error)
  process.exit(1)
})
