import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { spawnNpmSync } from "./lib/process-utils.mjs";
import {
  resolveApiSource,
  resolveHostedControlUrl,
} from "./lib/api-source.mjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, "..");

const args = new Set(process.argv.slice(2));
const skipInstall = args.has("--skip-install");
const skipEnv = args.has("--skip-env");

const ROOT_PROJECT = { name: "Nexus Ecosystem Root", dir: "." };

const EXTRA_PROJECTS = [
  { name: "@nexus/core", dir: "packages/nexus-core" },
  { name: "Nexus Main", dir: "Nexus Main" },
  { name: "Nexus Mobile", dir: "Nexus Mobile" },
  { name: "Nexus Code", dir: "Nexus Code" },
  { name: "Nexus Code Mobile", dir: "Nexus Code Mobile" },
];

const APP_ENV_TARGETS = [
  { appId: "main", dir: "Nexus Main" },
  { appId: "mobile", dir: "Nexus Mobile" },
  { appId: "code", dir: "Nexus Code" },
  { appId: "code-mobile", dir: "Nexus Code Mobile" },
];
const APP_INGEST_ENV_KEY_BY_APP = {
  main: "NEXUS_INGEST_KEY_MAIN",
  mobile: "NEXUS_INGEST_KEY_MOBILE",
  code: "NEXUS_INGEST_KEY_CODE",
  "code-mobile": "NEXUS_INGEST_KEY_CODE_MOBILE",
};

const DEFAULT_CONTROL_URL = resolveHostedControlUrl();
const INSECURE_INGEST_KEY_PATTERN = /(local-dev-key|dev-ingest-key|test-ingest-key)/i;
const PLACEHOLDER_INGEST_KEY_PATTERN = /^REPLACE_WITH_/i;

const runNpm = (cmdArgs, cwd) => {
  const printable = ["npm", ...cmdArgs].join(" ");
  console.log(`\n$ ${printable}`);

  const result = spawnNpmSync(cmdArgs, {
    cwd,
    stdio: "inherit",
    env: process.env,
  });

  if (result.error) {
    throw result.error;
  }

  if (result.status !== 0) {
    throw new Error(`Command fehlgeschlagen (${result.status}): ${printable}`);
  }
};

const fileExists = async (target) => {
  try {
    await fs.access(target);
    return true;
  } catch {
    return false;
  }
};

const normalizeEnvValue = (raw) =>
  String(raw || "")
    .trim()
    .replace(/^["']|["']$/g, "");

const getEnvLineValue = (raw, key) => {
  const match = raw.match(new RegExp(`^\\s*${key}\\s*=\\s*(.*)$`, "m"));
  if (!match) return null;
  return normalizeEnvValue(match[1]);
};

const setEnvLine = (raw, key, value) => {
  const line = `${key}=${value}`;
  const pattern = new RegExp(`^\\s*${key}\\s*=\\s*.*$`, "m");
  if (pattern.test(raw)) {
    return raw.replace(pattern, line);
  }
  const base = raw.endsWith("\n") ? raw : `${raw}\n`;
  return `${base}${line}\n`;
};

const resolveDesiredIngestKey = (appId) => {
  const envName = APP_INGEST_ENV_KEY_BY_APP[appId];
  const fromEnv = String(process.env[envName] || "").trim();
  if (fromEnv) return fromEnv;

  if (envName) return `REPLACE_WITH_${envName}`;
  return `REPLACE_WITH_NEXUS_INGEST_KEY_${String(appId).toUpperCase().replace(/-/g, "_")}`;
};

const ensureEnvFile = async ({ appId, dir }) => {
  const file = path.join(ROOT, dir, ".env.local");
  const defaults = {
    VITE_NEXUS_CONTROL_URL: DEFAULT_CONTROL_URL,
    VITE_NEXUS_CONTROL_INGEST_KEY: resolveDesiredIngestKey(appId),
  };

  if (!(await fileExists(file))) {
    const body = [
      "# Auto-generated by tools/setup-ecosystem.mjs",
      ...Object.entries(defaults).map(([key, value]) => `${key}=${value}`),
      "",
    ].join("\n");
    await fs.writeFile(file, body, "utf8");
    return { file, mode: "created", addedKeys: Object.keys(defaults) };
  }

  const raw = await fs.readFile(file, "utf8");
  const currentUrl = getEnvLineValue(raw, "VITE_NEXUS_CONTROL_URL");
  const currentIngest = getEnvLineValue(raw, "VITE_NEXUS_CONTROL_INGEST_KEY");
  let nextRaw = raw;
  const addedKeys = [];
  let changed = false;

  if (!currentUrl) {
    nextRaw = setEnvLine(nextRaw, "VITE_NEXUS_CONTROL_URL", defaults.VITE_NEXUS_CONTROL_URL);
    addedKeys.push("VITE_NEXUS_CONTROL_URL");
    changed = true;
  } else if (currentUrl !== DEFAULT_CONTROL_URL) {
    nextRaw = setEnvLine(nextRaw, "VITE_NEXUS_CONTROL_URL", defaults.VITE_NEXUS_CONTROL_URL);
    changed = true;
  }

  const shouldUpdateIngest =
    !currentIngest
    || INSECURE_INGEST_KEY_PATTERN.test(currentIngest)
    || (PLACEHOLDER_INGEST_KEY_PATTERN.test(currentIngest)
      && !PLACEHOLDER_INGEST_KEY_PATTERN.test(defaults.VITE_NEXUS_CONTROL_INGEST_KEY));

  if (!currentIngest) {
    nextRaw = setEnvLine(
      nextRaw,
      "VITE_NEXUS_CONTROL_INGEST_KEY",
      defaults.VITE_NEXUS_CONTROL_INGEST_KEY,
    );
    addedKeys.push("VITE_NEXUS_CONTROL_INGEST_KEY");
    changed = true;
  } else if (shouldUpdateIngest && currentIngest !== defaults.VITE_NEXUS_CONTROL_INGEST_KEY) {
    nextRaw = setEnvLine(
      nextRaw,
      "VITE_NEXUS_CONTROL_INGEST_KEY",
      defaults.VITE_NEXUS_CONTROL_INGEST_KEY,
    );
    changed = true;
  }

  if (!changed) {
    return { file, mode: "unchanged", addedKeys: [] };
  }
  await fs.writeFile(file, nextRaw, "utf8");
  return {
    file,
    mode: "updated",
    addedKeys,
  };
};

const installRootAndProjects = async () => {
  console.log(`\nInstalliere Dependencies: ${ROOT_PROJECT.name}`);
  runNpm(["install"], ROOT);

  for (const project of EXTRA_PROJECTS) {
    const projectDir = path.join(ROOT, project.dir);

    if (!(await fileExists(projectDir))) {
      console.warn(
        `- Uebersprungen: ${project.name} (${project.dir} nicht gefunden)`,
      );
      continue;
    }

    const packageJson = path.join(projectDir, "package.json");
    if (!(await fileExists(packageJson))) {
      console.warn(`- Uebersprungen: ${project.name} (keine package.json)`);
      continue;
    }

    console.log(`\nInstalliere Dependencies: ${project.name}`);
    runNpm(["install"], projectDir);
  }
};

const main = async () => {
  console.log("Nexus Ecosystem Setup");
  console.log("=====================");

  const apiSource = await resolveApiSource({
    root: ROOT,
    quiet: false,
  });

  if (!skipInstall) {
    await installRootAndProjects();
  } else {
    console.log("\nInstall-Schritt uebersprungen (--skip-install).");
  }

  const envResults = [];
  if (!skipEnv) {
    for (const target of APP_ENV_TARGETS) {
      const result = await ensureEnvFile(target);
      envResults.push(result);
    }
  } else {
    console.log("\nEnv-Schritt uebersprungen (--skip-env).");
  }

  console.log("\nSetup abgeschlossen.");
  console.log(`\nAPI-Modus: ${apiSource.mode}`);
  console.log(`API-Client: ${path.relative(ROOT, apiSource.apiDir)}`);
  console.log(`Hosted API URL: ${apiSource.controlBaseUrl}`);

  if (envResults.length > 0) {
    console.log("\n.env.local Status:");
    for (const result of envResults) {
      const rel = path.relative(ROOT, result.file);
      const keys =
        result.addedKeys.length > 0 ? ` (${result.addedKeys.join(", ")})` : "";
      console.log(`- ${rel}: ${result.mode}${keys}`);
    }
  }

  console.log("\nNaechste Schritte:");
  console.log(
    "- npm run dev:all                 # startet Main + Code (Hosted API)",
  );
  console.log(
    "- npm run dev:all:with-control-ui # startet Control UI + Main + Code",
  );
  console.log(
    "- npm run dev:control:open        # startet Control UI und oeffnet Browser",
  );
  console.log(
    "- npm run dev:main                # startet Nexus Main mit Electron",
  );
  console.log("- npm run build:ecosystem         # Full Build");
  console.log(
    "- npm run build:ecosystem:with-healthcheck  # Build inkl. Hosted-API Healthcheck",
  );
  console.log(
    "- npm run doctor:release          # prueft Hosted-API, Android SDK und Notarization-Readiness",
  );
  console.log(
    "- npm run verify:ecosystem        # fuehrt Integritaetspruefungen aus",
  );
};

main().catch((error) => {
  console.error("\nSetup fehlgeschlagen:");
  console.error(error.message || error);
  process.exit(1);
});
